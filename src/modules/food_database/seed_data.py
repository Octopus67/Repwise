# ── Serving options enrichment ─────────────────────────────────────────────
# Assigns category-appropriate serving_options to each food item.
# These are stored in micro_nutrients._serving_options when seeded.


def _build_serving_options(item: dict) -> list[dict]:
    """Generate serving options for a food item based on its category and name."""
    cat = item["category"]
    name = item["name"]
    ss = item["serving_size"]

    if cat == "Bread":
        return [
            {"label": "1 piece", "grams": ss, "is_default": True},
            {"label": "2 pieces", "grams": ss * 2},
        ]

    if cat == "Breakfast":
        # Multi-piece items
        return [
            {"label": "1 piece", "grams": ss, "is_default": True},
            {"label": "2 pieces", "grams": ss * 2},
        ]

    if cat == "Grain":
        return [
            {"label": "1 cup", "grams": 150.0, "is_default": True},
            {"label": "1 plate", "grams": 250.0},
        ]

    if cat == "Rice Dish":
        return [
            {"label": "1 plate", "grams": ss, "is_default": True},
            {"label": "½ plate", "grams": round(ss / 2)},
        ]

    if cat in ("Curry", "Dal"):
        return [
            {"label": "1 katori", "grams": 150.0 if ss <= 200 else 200.0, "is_default": True},
            {"label": "1 bowl", "grams": 250.0 if ss <= 200 else 300.0},
        ]

    if cat == "Protein":
        return [
            {"label": "1 serving", "grams": ss, "is_default": True},
            {"label": "½ serving", "grams": round(ss / 2)},
        ]

    if cat == "Snack":
        # Composite snacks (Pav Bhaji, Misal Pav, etc.) get "1 plate"
        if any(kw in name for kw in ("Pav Bhaji", "Misal Pav", "Bhel Puri", "Litti")):
            return [
                {"label": "1 plate", "grams": ss, "is_default": True},
                {"label": "½ plate", "grams": round(ss / 2)},
            ]
        return [
            {"label": "1 piece", "grams": ss, "is_default": True},
            {"label": "2 pieces", "grams": ss * 2},
        ]

    if cat == "Dessert":
        return [
            {"label": "1 piece", "grams": ss, "is_default": True},
            {"label": "2 pieces", "grams": ss * 2},
        ]

    if cat == "Beverage":
        return [
            {"label": "1 glass", "grams": 200.0, "is_default": True},
            {"label": "1 cup", "grams": 150.0},
        ]

    if cat == "Side":
        # Chutneys, pickles, ghee, butter → tablespoon
        if any(kw in name for kw in ("Chutney", "Pickle", "Ghee", "Butter")):
            return [
                {"label": "1 tbsp", "grams": 15.0, "is_default": True},
                {"label": "2 tbsp", "grams": 30.0},
            ]
        # Raita, papad → serving
        return [
            {"label": "1 serving", "grams": ss, "is_default": True},
            {"label": "½ serving", "grams": round(ss / 2)},
        ]

    if cat == "Health Food":
        # Treat like breakfast/grain depending on unit
        if item["serving_unit"] == "ml":
            return [
                {"label": "1 bowl", "grams": ss, "is_default": True},
                {"label": "½ bowl", "grams": round(ss / 2)},
            ]
        return [
            {"label": "1 serving", "grams": ss, "is_default": True},
            {"label": "½ serving", "grams": round(ss / 2)},
        ]

    # Fallback
    return [
        {"label": "1 serving", "grams": ss, "is_default": True},
    ]


# Enrich every item: store serving_options at top level AND inside
# micro_nutrients._serving_options for DB persistence (avoids schema migration).
for _item in INDIAN_FOOD_ITEMS:
    _item["serving_options"] = _build_serving_options(_item)
    if _item.get("micro_nutrients") is None:
        _item["micro_nutrients"] = {}
    _item["micro_nutrients"]["_serving_options"] = _item["serving_options"]


